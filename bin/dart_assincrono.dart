/*
Dart é síncrono. Dart é uma linguagem Single Thread, ou seja, que realiza uma tarefa por vez.

Isolates (blocos): espaços de memória, dedicados para realizar tarefas no computador. Exemplo: abrir uma nova tela.
Loop de Eventos (engrenagem): gerencia a pilha de tarefas para execução.

Assincronismo: técnica que separa tarefas que precisam de um tempo de espera,
a fim de tornar eficiente a execução de tarefas rápidas, enquanto esperamos por tarefas lentas
(que esperam informação de fora, lugar de onde não temos controle).

Assincronismo no código é muito útil em projetos reais, pois permite que um aplicativo continue rodando
enquanto espera informações externas, ou seja, originadas de banco de dados, APIs e demais fontes de dados.
*/
